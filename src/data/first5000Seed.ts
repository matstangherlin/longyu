const JOURNEY_BY_HANZI = new Map(CHARACTERS.map((character) => [character.hanzi, character]));
export const FIRST_1200_ORDER = Array.from(new Set(Array.from(FIRST_FREQUENCY_ORDER)));

const toneMarks: Record<number, RegExp> = {
  1: /[āēīōūǖ]/u,
  2: /[áéíóúǘ]/u,
  3: /[ǎěǐǒǔǚ]/u,
  4: /[àèìòùǜ]/u,
  5: /^[a-zü]+$/iu,
};

const toneFromPinyin = (pinyin: string): First5000SeedEntry["tone"] => {
  for (const tone of [1, 2, 3, 4] as const) {
    if (toneMarks[tone].test(pinyin)) return tone;
  }

  return 5;
};

const tonelessFromPinyin = (pinyin: string) =>
  pinyin
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/ü/g, "u")
    .toLowerCase();

const toEntry = (hanzi: string, index: number): First5000SeedEntry => {
  const journeyCharacter = JOURNEY_BY_HANZI.get(hanzi);
  const pinyin = journeyCharacter?.pinyin ?? BASIC_PINYIN[hanzi] ?? "zì";
  const meaningPt = journeyCharacter?.meaningPt ?? BASIC_PT[hanzi] ?? "entrada de consulta; glosa em revisão";

  return {
    hanzi,
    pinyin,
    meaningEn: BASIC_EN[hanzi] ?? "consultation entry pending review",
    meaningPt,
    freqRank: index + 1,
    source: "first5000",
    inJourney: false,
    hasLesson: false,
    hasDecomposition: false,
    isPremium: false,
    tone: journeyCharacter?.tone ?? toneFromPinyin(pinyin),
    toneless: journeyCharacter?.toneless ?? tonelessFromPinyin(pinyin),
  };
};

export const FIRST_1200_SEED: First5000SeedEntry[] = FIRST_1200_ORDER.map(toEntry);

